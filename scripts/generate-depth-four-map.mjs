import fs from "node:fs";
import {
  PLAN_PATH,
  approvedSharedCandidates,
  markdownCell,
  nodeTitle,
  readPlan,
  titleCaseTemporalKind,
  validatePlan,
} from "./depth-four-plan-lib.mjs";

const OUTPUT_PATH = "docs/editorial/depth-four-map.md";

const plan = readPlan(process.argv[2] ?? PLAN_PATH);
const result = validatePlan(plan);

if (result.errors.length) {
  console.error("Cannot generate depth-four map because the planning JSON is invalid.");
  for (const error of result.errors) console.error(`ERROR ${error}`);
  process.exit(1);
}

const nodeById = new Map(plan.nodes.map((node) => [node.id, node]));
const strandById = new Map(plan.strands.map((strand) => [strand.id, strand]));
const approvedShared = approvedSharedCandidates(plan);
const edgesByStrandAndChild = new Map();

for (const edge of plan.edges) {
  const key = `${edge.strandId}:${edge.childId}`;
  const list = edgesByStrandAndChild.get(key) ?? [];
  list.push(edge);
  edgesByStrandAndChild.set(key, list);
}

const lines = [];
const out = (line = "") => lines.push(line);
const table = (headers, rows) => {
  out(`| ${headers.map(markdownCell).join(" | ")} |`);
  out(`| ${headers.map(() => "---").join(" | ")} |`);
  for (const row of rows) out(`| ${row.map(markdownCell).join(" | ")} |`);
  out("");
};

out("# Depth-Four Map");
out("");
out("<!-- GENERATED FROM docs/editorial/depth-four-plan.json. Do not edit this file directly. Run `npm run generate:depth-four-map` after changing the planning JSON. -->");
out("");
out("This is a generated planning map for eventual depth-four coverage. It does not add public nodes, public edges, public prose or sources.");
out("");
out("The source of truth is [depth-four-plan.json](depth-four-plan.json). The generated map calculates Coverage depth from navigable planning edges, so manual table counts cannot drift from the planned graph.");
out("");

out("## Scope And Guardrails");
out("");
out("This planning system separates canonical nodes, navigable edges, contextual strand placements, approved shared candidates, computed shared-node metrics, preview-only Also touches relationships, historical spines and implementation batches.");
out("");
out("The atlas remains a contextual knowledge graph, not a strict tree and not a proportional timeline. Time provides the backbone; causality and explanation provide the branches.");
out("");

out("## Planning Approval State");
out("");
out(`Planning status: \`${plan.planningStatus}\`.`);
out("");
out("The global depth-four plan is a draft inventory. Provisional relationships may remain in the inventory, but an implementation batch is not ready until its relevant nodes, navigable edges, contextual placements and approved shared contexts are explicitly reviewed.");
out("");

out("## Generated Metrics");
out("");
table(
  ["Metric", "Count", "Note"],
  [
    ["Unique canonical node titles", result.metrics.canonicalNodes, "Root Bitcoin excluded."],
    ["Navigable planning edges", result.metrics.navigableEdges, "Approved edge types only."],
    ["Strand placements", result.metrics.strandPlacements, "Contextual appearances across strand tables."],
    ["Approved shared candidates", result.metrics.approvedSharedCandidates, "Nodes the editorial plan intends to reuse in more than one context."],
    ["Realised multi-parent nodes", result.metrics.realisedMultiParentNodes.length, "Computed from two or more distinct incoming navigable parent edges."],
    ["Multi-strand placement nodes", result.metrics.multiStrandPlacementNodes.length, "Computed from canonical nodes placed in two or more strand tables."],
    ["Preview-only Also touches relationships", result.metrics.alsoTouches, "Informational, not navigable."],
    ["Blocked implementation batches", result.metrics.blockedImplementationBatches, "Computed from review status and batch gate status."],
    [
      "Revised canonical target",
      `${plan.canonicalTarget?.min ?? ""}-${plan.canonicalTarget?.max ?? ""}`,
      "Guidance, not a quota.",
    ],
  ],
);

out("### Review-State Metrics");
out("");
table(
  ["Scope", "Reviewed", "Provisional", "Total"],
  Object.entries(result.metrics.reviewMetrics).map(([scope, metric]) => [
    scope,
    metric.reviewed,
    metric.provisional,
    metric.total,
  ]),
);

out("### Shared-Node Accounting");
out("");
out("These are deliberately separate. Approved shared candidates are an editorial intention; realised multi-parent nodes are computed from navigable edges; multi-strand placement nodes are computed from contextual strand appearances.");
out("");
table(
  ["Concept", "Computed or declared?", "Count"],
  [
    ["Approved shared candidates", "Declared in planning JSON", result.metrics.approvedSharedCandidates],
    ["Realised multi-parent nodes", "Computed from navigable edges", result.metrics.realisedMultiParentNodes.length],
    ["Multi-strand placement nodes", "Computed from strand placements", result.metrics.multiStrandPlacementNodes.length],
  ],
);

table(
  ["Realised multi-parent node", "Distinct incoming parents"],
  result.metrics.realisedMultiParentNodes.map((item) => [
    nodeTitle(plan, item.nodeId),
    item.parentIds.map((parentId) => nodeTitle(plan, parentId)),
  ]),
);

table(
  ["Multi-strand placement node", "Strand placements"],
  result.metrics.multiStrandPlacementNodes.map((item) => [
    nodeTitle(plan, item.nodeId),
    item.strandIds.map((strandId) => strandById.get(strandId)?.title ?? strandId),
  ]),
);

out("### Unique Canonical Nodes By Calculated Coverage Depth");
out("");
table(
  ["Coverage depth", "Unique canonical nodes"],
  [1, 2, 3, 4].map((depth) => [depth, result.metrics.byDepth.get(depth) ?? 0]),
);

out("### Strand Placements By Calculated Coverage Depth");
out("");
table(
  ["Strand", "Depth 1", "Depth 2", "Depth 3", "Depth 4", "Strand placements"],
  plan.strands.map((strand) => {
    const placementMetrics = result.metrics.placementsByStrand.get(strand.id);
    return [
      strand.title,
      placementMetrics?.byDepth.get(1) ?? 0,
      placementMetrics?.byDepth.get(2) ?? 0,
      placementMetrics?.byDepth.get(3) ?? 0,
      placementMetrics?.byDepth.get(4) ?? 0,
      placementMetrics?.total ?? 0,
    ];
  }),
);

out("### Unique Canonical Nodes By Temporal Kind");
out("");
table(
  ["Temporal kind", "Unique canonical nodes"],
  ["persistent", "period", "atemporal", "recurring", "event"].map((kind) => [
    titleCaseTemporalKind(kind),
    result.metrics.byTemporalKind.get(kind) ?? 0,
  ]),
);

out("## Table Conventions");
out("");
out("- Existing node: already present in `content/nodes.json`; do not duplicate it.");
out("- Candidate: proposed future canonical node; requires a bubble proposal before implementation.");
out("- Coverage depth: calculated shortest route from `bitcoin` through navigable planning edges.");
out("- Historical-spine membership: `Yes` means the node belongs on the approved backbone for that strand; `Branch` means it explains or attaches to the spine.");
out("- Shared: `Approved shared candidate` means the editorial plan intends to reuse the canonical node in more than one context. `Also touches only` means preview-only.");
out("- Review status: `provisional` means not yet semantically approved; `reviewed-for-batch` and `approved-for-implementation` are the only states that satisfy a batch gate.");
out("- Time confidence: High, Medium, Low, Contested or Not applicable.");
out("");

out("## Historical Spines");
out("");
out("Every main strand requires one approved historical spine before public nodes are added. The spine is a backbone, not the whole graph. Mechanisms and examples branch from it, and the spine must not imply simple replacement or universal progression.");
out("");

for (const strand of plan.strands) {
  const spine = plan.historicalSpines[strand.id];
  out(`### ${spine.title}`);
  out("");
  if (spine.note) {
    out(spine.note);
    out("");
  }
  table(
    ["Spine node", "Review status", "Temporal kind", "Broad era", "Time confidence", "Why it belongs on the spine", "Branches attached", "Risks"],
    spine.nodes.map((item) => {
      const node = nodeById.get(item.nodeId);
      return [
        node?.title ?? nodeTitle(plan, item.nodeId),
        spine.reviewStatus,
        node ? titleCaseTemporalKind(node.temporalKind) : "Persistent",
        node?.broadEra ?? item.broadEra ?? "",
        node?.timeConfidence ?? item.timeConfidence ?? "",
        item.rationale,
        item.branchesAttached ?? [],
        item.risks,
      ];
    }),
  );
}

out("## Approved Shared Candidates");
out("");
out(`Target: approximately 15-20 approved shared candidates. This revision approves ${approvedShared.length}.`);
out("");
table(
  ["Canonical node", "Review status", "Intended route contexts", "Why approved"],
  approvedShared.map((shared) => [
    nodeTitle(plan, shared.nodeId),
    shared.reviewStatus,
    shared.contexts,
    shared.rationale,
  ]),
);

out("## Preview-Only Also Touches Register");
out("");
out(`These ${plan.alsoTouches.length} relationships are informational previews only. They should not become navigable edges without a new proposal and human review.`);
out("");
table(
  ["Relationship", "Review status", "Canonical status", "Reason"],
  plan.alsoTouches.map((touch) => [
    `${touch.sourceTitle} also touches ${touch.targetTitle}`,
    touch.reviewStatus,
    `${touch.sourceNodeId ? "canonical source" : "non-canonical source"}; ${touch.targetNodeId ? "canonical target" : "non-canonical target"}`,
    touch.reason,
  ]),
);

out("## Strand Tables");
out("");

for (const strand of plan.strands) {
  out(`### ${strand.title}`);
  out("");
  const placements = plan.placements.filter((placement) => placement.strandId === strand.id);
  table(
    [
      "Candidate node",
      "Type",
      "Coverage depth",
      "Parent(s)",
      "Edge type(s)",
      "Historical-spine?",
      "Temporal kind",
      "Broad era",
      "Time confidence",
      "Shared?",
      "Review status",
      "Score",
      "Source categories",
      "Status",
    ],
    placements.map((placement) => {
      const node = nodeById.get(placement.nodeId);
      const incoming = edgesByStrandAndChild.get(`${placement.strandId}:${placement.nodeId}`) ?? [];
      return [
        node.title,
        node.type,
        result.depths.get(node.id),
        incoming.map((edge) => nodeTitle(plan, edge.parentId)),
        incoming.map((edge) => edge.type),
        placement.role === "spine" ? "Yes" : "Branch",
        titleCaseTemporalKind(node.temporalKind),
        node.broadEra,
        node.timeConfidence,
        placement.sharedStatus,
        placement.reviewStatus,
        node.score,
        node.sourceCategories,
        node.planningStatus,
      ];
    }),
  );
}

out("## Revision Decision Log");
out("");

for (const section of plan.decisionLog) {
  out(`### ${section.title}`);
  out("");
  table(
    section.columns,
    section.entries.map((entry) => section.columns.map((column) => entry[column] ?? "")),
  );
}

if (plan.sharedAccountingSummary) {
  out("### Shared-Node Accounting Note");
  out("");
  out(plan.sharedAccountingSummary);
  out("");
}

out("## Future Implementation Batches");
out("");
out("These batches do not implement anything. Each batch still requires bubble proposals, source inventories, human approval, validation, manual editorial review and a commit.");
out("");
table(
  ["Batch", "Gate status", "Review blockers", "Nodes", "Why this order"],
  plan.implementationBatches.map((batch) => {
    const gate = result.metrics.batchGates.find((item) => item.id === batch.id);
    return [
    `${batch.order}. ${batch.title}`,
      batch.gateStatus,
      gate?.blockerCount ?? "",
    batch.nodeIds.map((nodeId) => nodeTitle(plan, nodeId)),
    batch.rationale,
    ];
  }),
);

if (plan.nextBatchNote) {
  out(plan.nextBatchNote);
  out("");
}

out("## Unresolved Editorial Questions");
out("");
for (const question of plan.unresolvedQuestions ?? []) out(`- ${question}`);
out("");

out("## Definition Of Planning Done");
out("");
out("This map is ready for human pruning when:");
out("");
for (const item of plan.definitionOfPlanningDone ?? []) out(`- ${item}`);

fs.writeFileSync(OUTPUT_PATH, `${lines.join("\n")}\n`);
console.log(`Generated ${OUTPUT_PATH} from ${PLAN_PATH}.`);
